import fs from 'node:fs';
import path from 'node:path';

const rootDir = process.cwd();
const specPath = path.join(rootDir, 'scripts', 'capture-screenshots.spec.ts');

function walkMarkdownFiles(dir) {
  const result = [];
  const entries = fs.readdirSync(dir, { withFileTypes: true });

  for (const entry of entries) {
    if (entry.name === 'node_modules' || entry.name === '.git' || entry.name === '.vitepress' || entry.name === 'test-results') {
      continue;
    }

    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      result.push(...walkMarkdownFiles(fullPath));
      continue;
    }

    if (entry.isFile() && entry.name.endsWith('.md')) {
      result.push(fullPath);
    }
  }

  return result;
}

function extractScriptScreenshotPaths(content) {
  const pattern = /shot\(page,\s*(CONSOLE_SCREENSHOTS_DIR|BOSS_SCREENSHOTS_DIR),\s*'([^']+)'\)/g;
  const paths = new Set();
  let match = pattern.exec(content);

  while (match) {
    const group = match[1].includes('CONSOLE') ? 'console' : 'boss';
    const name = match[2];
    paths.add(`/screenshots/${group}/${name}.png`);
    match = pattern.exec(content);
  }

  return paths;
}

function extractDocScreenshotPaths(content) {
  const paths = new Set();
  const markdownPattern = /!\[[^\]]*\]\((\/screenshots\/[^)\s]+)\)/g;
  const htmlImgPattern = /<img[^>]*src=["'](\/screenshots\/[^"']+)["'][^>]*>/g;

  let match = markdownPattern.exec(content);
  while (match) {
    if (/\.(png|jpe?g|webp|gif|svg)$/i.test(match[1])) {
      paths.add(match[1]);
    }
    match = markdownPattern.exec(content);
  }

  match = htmlImgPattern.exec(content);
  while (match) {
    if (/\.(png|jpe?g|webp|gif|svg)$/i.test(match[1])) {
      paths.add(match[1]);
    }
    match = htmlImgPattern.exec(content);
  }

  return paths;
}

function toSortedArray(setValue) {
  return [...setValue].sort((a, b) => a.localeCompare(b));
}

function diffSet(left, right) {
  const result = new Set();
  for (const value of left) {
    if (!right.has(value)) {
      result.add(value);
    }
  }
  return result;
}

const scriptContent = fs.readFileSync(specPath, 'utf8');
const scriptPaths = extractScriptScreenshotPaths(scriptContent);

const mdFiles = walkMarkdownFiles(rootDir);
const docPaths = new Set();
for (const filePath of mdFiles) {
  const content = fs.readFileSync(filePath, 'utf8');
  const extracted = extractDocScreenshotPaths(content);
  for (const item of extracted) {
    docPaths.add(item);
  }
}

const scriptScopePattern = /^\/screenshots\/(console|boss)\/[^/]+\.png$/;
const docFlatScope = new Set([...docPaths].filter((item) => scriptScopePattern.test(item)));

const scriptNotInDoc = diffSet(scriptPaths, docPaths);
const docFlatNotInScript = diffSet(docFlatScope, scriptPaths);
const docAnyNotInScript = diffSet(docPaths, scriptPaths);

console.log('Screenshot consistency report');
console.log('============================');
console.log(`Script outputs: ${scriptPaths.size}`);
console.log(`Doc references (unique): ${docPaths.size}`);
console.log(`Doc references in flat scope (/screenshots/console|boss/*.png): ${docFlatScope.size}`);
console.log('');
console.log(`Missing in docs (script -> doc): ${scriptNotInDoc.size}`);
console.log(`Unknown in script (doc flat scope -> script): ${docFlatNotInScript.size}`);
console.log(`Unknown in script (all doc refs -> script): ${docAnyNotInScript.size}`);

if (scriptNotInDoc.size > 0) {
  console.log('\n[script -> doc] Missing references:');
  for (const item of toSortedArray(scriptNotInDoc)) {
    console.log(`- ${item}`);
  }
}

if (docFlatNotInScript.size > 0) {
  console.log('\n[doc flat scope -> script] Not generated by current script:');
  for (const item of toSortedArray(docFlatNotInScript)) {
    console.log(`- ${item}`);
  }
}

if (docAnyNotInScript.size > 0) {
  console.log('\n[doc all refs -> script] Sample (first 40):');
  for (const item of toSortedArray(docAnyNotInScript).slice(0, 40)) {
    console.log(`- ${item}`);
  }
}

if (scriptNotInDoc.size > 0 || docFlatNotInScript.size > 0) {
  process.exit(1);
}

console.log('\nOK: script outputs and doc flat-scope references are aligned.');
